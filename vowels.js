
const word1 = 'hello';
let numOfvowels = 0;
let numOfcons = 0;
const a = 'a';
const e = 'e';
const i = 'i';
const o = 'o';
const u = 'u';
//follow each number of vowels and consonants
const vowels = ['a', 'i', 'e', 'o', 'u'];
// over the .length of word count vowels;
for (let index = 0; index < word1.length; index++){
    const currLetter = word1[index]
    if (currLetter === a || currLetter === e || currLetter === i || currLetter === o || currLetter === u) {
        numOfvowels++
    } else {
        numOfcons++
    }
}
console.log(word1 + ' has ' + numOfcons + ' consonants and ' + numOfvowels + ' vowels')


